package com.example.demo;

public class Hisrow {
    private String c1;
    private double c2;
    private String c3;

    public Hisrow(String c1, double c2, String c3) {
        this.c1 = c1;
        this.c2 = c2;
        this.c3 = c3;
    }
    public Hisrow(){

    }

    public String getC1() {
        return c1;
    }

    public void setC1(String c1) {
        this.c1 = c1;
    }

    public double getC2() {
        return c2;
    }

    public void setC2(double c2) {
        this.c2 = c2;
    }

    public String getC3() {
        return c3;
    }

    public void setC3(String c3) {
        this.c3 = c3;
    }
}
